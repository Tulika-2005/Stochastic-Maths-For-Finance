import numpy as np

P = np.array([
    [0.60,0.30,0.10,0.00],
    [0.20,0.50,0.20,0.10],
    [0.10,0.30,0.40,0.20],
    [0.05,0.15,0.30,0.50]
])

n_steps = 10000

state = 0

path = [state]

for _ in range(n_steps):

    state = np.random.choice(
        [0,1,2,3],
        p=P[state]
    )

    path.append(state)

"""## Part (g)"""

path = np.array(path)

for s in range(4):

    print(
        s,
        np.mean(path==s)
    )

"""## Part (h)"""

returns = np.array([
    0.012,
    0.004,
   -0.006,
   -0.025
])

empirical_return = np.mean(
    returns[path]
)

print(empirical_return)

"""The result is nearly same as calculated theoretical value.

## Part (i)
"""

import matplotlib.pyplot as plt

cum_return = np.cumsum(
    returns[path]
)

plt.plot(cum_return)

plt.xlabel("Week")
plt.ylabel("Cumulative Return")

plt.title("Portfolio Performance")

plt.show()

