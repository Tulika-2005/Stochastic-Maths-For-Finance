import numpy as np

P = np.array([
    [0.60,0.30,0.10,0.00],
    [0.20,0.50,0.20,0.10],
    [0.10,0.30,0.40,0.20],
    [0.05,0.15,0.30,0.50]
])

eigvals,eigvecs=np.linalg.eig(P.T) # It finds all eigenvalues and eigenvectors of P^T.
# normally python's eigen vector equation solves for column vector,but here pie is a row vector so we take transpose of the matrix.

idx=np.argmin(np.abs(eigvals-1))
# For every Markov chain there is always an eigenvalue 1, so finds the eigenvalue closest to 1.

pi=np.real(eigvecs[:,idx]) # extracts the corresponding eigenvector.Sum is not 1. so we divide this whole by the total sum.

pi=pi/pi.sum()

print(pi)

